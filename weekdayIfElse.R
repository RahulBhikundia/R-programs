day = as.integer(readline("Enter Number(1-7):"))

if(day == 1){
  cat("Sunday")
} else if(day == 2){
  cat("Monday")
} else if(day == 3){
  cat("Tuesday")
} else if(day == 4){
  cat("Wednesday")
} else if(day == 5){
  cat("Thursday")
} else if(day == 6){
  cat("Friday")
} else if(day == 7){
  cat("Saturday")
}